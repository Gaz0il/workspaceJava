package model;

public class Dede2D {
	private int x;
	private int y;

	/**
	 * @param x
	 * @param y
	 */
	public Dede2D(int x, int y) {
		this.x = x;
		this.y = y;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "Dede2D [x=" + x + ", y=" + y + "]";
	}

}
